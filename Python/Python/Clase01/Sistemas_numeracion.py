#Profundizando en los sistemas de numeracion

#Sistema decimal
a = 10
print(f"decimal = {a}")

#Sistema binario
b = 0b1010
print(f"binario = {b}")

#Sistema octal
c = 0o12
print(f"octal = {c}")

#Sistema hexadecimal
d = 0xA
print(f"hexadecimal = {d}")

#Base Decimal
a = int('23', 10)
print(f"base decimal = {a}")

#Base Binario
b = int('10111', 2)
print(f"base binaria = {b}")

#Base Octal
c = int('27', 8)
print(f"base octal = {c}")

#Base Hexadecimal
d = int('17', 16)
print(f"base hexadecimal = {d}")

#Base 5
#Sus valores son de 0 a 4
a = int('343', 5)
print(f"base 5 = {a}")